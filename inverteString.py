def inverter_string(texto):
    return texto[::-1]

# Exemplo de uso
string_original = input("Digite uma string: ")
string_invertida = inverter_string(string_original)
print("String invertida:", string_invertida)

#Neste código, a função inverter_string recebe uma string como entrada e retorna a string invertida usando a técnica de slicing com passo -1. 
#O exemplo de uso solicita uma string ao usuário, chama a função para inverter a string e, em seguida, exibe a string invertida.





